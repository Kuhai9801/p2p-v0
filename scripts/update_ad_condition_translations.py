TIER_KEYS_TO_REMOVE = [
    "minimumSellerTier",
    "minimumSellerTierHelper",
    "minimumBuyerTier",
    "minimumBuyerTierHelper",
    "minimumTierSheetTitle",
    "tierAllTiers",
    "tierAllTiersDescription",
    "tierSilverAndAbove",
    "tierSilverDescription",
    "tierGoldAndAbove",
    "tierGoldDescription",
    "tierDiamondOnly",
    "tierDiamondDescription",
]


def update_locale(locale: str) -> None:
    path = TRANSLATIONS_DIR / f"{locale}.json"
    data = json.loads(path.read_text(encoding="utf-8"))
    ad_form = data.setdefault("adForm", {})

    for key in TIER_KEYS_TO_REMOVE:
        ad_form.pop(key, None)

    if locale == "en":
        return

    for key, translations in NEW_KEYS.items():
        if locale in translations:
            ad_form[key] = translations[locale]

    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    for path in sorted(TRANSLATIONS_DIR.glob("*.json")):
        update_locale(path.stem)
        print(f"updated {path.name}")


if __name__ == "__main__":
    main()
